    df_ohe.to_csv(os.path.join(cfg.OUT_DIR, "metadata_ohe.csv"), index=False)
    A = build_adjacency(df_meta)
    W = row_stochastic_W(A, self_mix_alpha=cfg.SELF_MIX_ALPHA)
    if cfg.USE_GUDMUNDSON:
        dist = _all_pairs_shortest_paths(A)
        R = _corr_from_hops(dist, cfg.CORR_LEN_HOPS)
        GLOBAL_CORR_CHOL = _robust_cholesky(R)
    else:
        GLOBAL_CORR_CHOL = None
    pd.DataFrame(A, index=df_meta["node"], columns=df_meta["node"]).to_csv(os.path.join(cfg.OUT_DIR, "network_adjacency.csv"))
    pd.DataFrame(W, index=df_meta["node"], columns=df_meta["node"]).to_csv(os.path.join(cfg.OUT_DIR, "network_weights_W.csv"))
    with open(os.path.join(cfg.OUT_DIR, "reason_vocab.json"), "w") as f:
        json.dump(REASON_VOCAB, f, indent=2)
    splits = {"train": cfg.T_TRAIN, "val": cfg.T_VAL, "test": cfg.T_TEST}
    df_splits: Dict[str, pd.DataFrame] = {}
    all_windows: List[Dict] = []
    for split_name, T in splits.items():
        seed = cfg.SEED_BASE + {"train": 11, "val": 23, "test": 37}[split_name]
        df, windows = generate_split(df_meta, A, split_name, T_export=T, seed=seed)
        df_splits[split_name] = df
        all_windows.extend(windows)
        eligible = set(attack_eligible_nodes(df_meta))
        mask_elig = df["node"].isin(list(eligible))
        cov_overall = float(df.loc[mask_elig, "attack_label"].mean())
        active_elig_rows = int(((df["tx_count"] > 0) & mask_elig).sum())
        attack_active_rows = int(((df["attack_label"] == 1) & (df["tx_count"] > 0) & mask_elig).sum())
        cov_active = float(attack_active_rows / max(1, active_elig_rows))
        silent_attack_rows = int(((df["attack_label"] == 1) & (df["tx_count"] == 0) & mask_elig).sum())
        attack_rows = int(((df["attack_label"] == 1) & mask_elig).sum())
        silent_attack = float(silent_attack_rows / max(1, attack_rows))
        print(f"[{split_name.upper()}] coverage (overall eligible rows): {cov_overall:.4f}")
        print(f"[{split_name.upper()}] coverage (ACTIVE eligible rows):  {cov_active:.4f} (target={cfg.TARGET_ATTACK_FRAC})")
        print(f"[{split_name.upper()}] silent fraction among y=1 (eligible): {silent_attack:.4f}")
    neighbor_cols = ["snr_db", "latency_smooth", "csi_amp", "packet_error", "phase_sin", "phase_cos", "dphase"]
    for split_name, df in df_splits.items():
        df_splits[split_name] = compute_neighbor_features(df, W, cols=neighbor_cols)
    for split_name in df_splits.keys():
        df_splits[split_name] = add_sensitivity_features(df_splits[split_name])
    z_cols = [
        "snr_db", "packet_error", "latency", "latency_smooth", "csi_amp",
        "csi_entropy", "csi_drift", "csi_skewness", "csi_kurtosis",
        "tx_count", "time_since_last_tx",
        "phase_sin", "phase_cos", "dphase",
        "avg_neighbor_snr_db", "avg_neighbor_latency_smooth", "avg_neighbor_csi_amp", "avg_neighbor_packet_error",
        "avg_neighbor_phase_sin", "avg_neighbor_phase_cos", "avg_neighbor_dphase",
        "rho_like_snr_db", "rho_like_latency_smooth", "rho_like_csi_amp", "rho_like_packet_error",
        "rho_like_phase_sin", "rho_like_phase_cos", "rho_like_dphase",
        "csi_db", "dcsi_db", "csi_ac1", "csi_innov_var", "dcsi_qspread", "dcsi_iqr",
        "dsnr_db", "snr_ac1", "snr_innov_var",
        "snr_minus_csi_db", "mad_snr_minus_csi_db",
        "per_logit", "dper_logit",
        "lat_log", "dlat_log", "mad_lat_log",
    ]
    train_stats_df = df_splits["train"].copy()
    train_stats_df = train_stats_df[(train_stats_df["attack_label"] == 0) & (train_stats_df["tx_count"] > 0)]
    stats = fit_zscore_per_node(train_stats_df, z_cols)
    base_clients = os.path.join(cfg.OUT_DIR, "clients")
    ensure_dir(base_clients)
    for node in range(len(NODES)):
        d = os.path.join(base_clients, f"client_node{node}")
        ensure_dir(d)
        with open(os.path.join(d, "train_stats.json"), "w") as f:
            json.dump({"node": int(node), "stats": {c: [float(mu), float(sd)] for c, (mu, sd) in stats[int(node)].items()}}, f, indent=2)
    for split_name in ("train", "val", "test"):
        df_splits[split_name] = apply_zscore(df_splits[split_name], stats, z_cols, prefix="z_")
    for split_name in ("train", "val", "test"):
        df_splits[split_name] = add_cusum_ewma_and_graph(df_splits[split_name])
    for split_name in ("train", "val", "test"):
        write_clients(df_splits[split_name], cfg.OUT_DIR, split_name)
    win_rows = []
