def compute_per_from_snr_db(meta: pd.DataFrame, snr_db: np.ndarray) -> np.ndarray:
    snr50, k = per_node_per_params(meta)
    per = 1.0 / (1.0 + np.exp(k * (snr_db - snr50)))
    return np.clip(per, 1e-5, 1.0 - 1e-5).astype(np.float32)

def compute_latency_ms(meta: pd.DataFrame, per: np.ndarray) -> np.ndarray:
    n, T = per.shape
    exp_rtx = per / np.maximum(1.0 - per, 1e-4)
    exp_rtx = np.clip(exp_rtx, 0.0, cfg.MAX_RTX_EXPECT).astype(np.float32)
    base_lat = np.zeros((n, 1), dtype=np.float32)
    jitter_std = np.zeros((n, 1), dtype=np.float32)
    spike_p0 = np.zeros((n, 1), dtype=np.float32)
    spike_scale = np.zeros((n, 1), dtype=np.float32)
    for i in range(n):
        tech = str(meta.loc[i, "tech"])
        b, js, p0, sc = per_tech_latency_params(tech)
        base_lat[i, 0] = float(b); jitter_std[i, 0] = float(js); spike_p0[i, 0] = float(p0); spike_scale[i, 0] = float(sc)
    jitter = (jitter_std * np.random.randn(n, T)).astype(np.float32)
    p1 = np.clip(0.03 + 0.06 * (spike_scale / np.maximum(spike_scale.mean(), 1e-3)), 0.02, 0.12).astype(np.float32)
    p = np.clip(spike_p0 + p1 * per, 0.0, 0.35).astype(np.float32)
    u = np.random.rand(n, T).astype(np.float32)
    shock = (u < p).astype(np.float32) * (np.random.exponential(scale=1.0, size=(n, T)).astype(np.float32))
    shock = shock * spike_scale
    spikes = np.zeros((n, T), dtype=np.float32)
    spikes[:, 0] = shock[:, 0]
    ar = float(cfg.LAT_SPIKE_AR)
    for t in range(1, T):
        spikes[:, t] = ar * spikes[:, t - 1] + shock[:, t]
    lat = base_lat + cfg.RTX_MS * exp_rtx + jitter + spikes
    lat = np.clip(lat, 0.5, None).astype(np.float32)
    return lat

def smooth_ewma(x: np.ndarray, beta: float) -> np.ndarray:
    y = np.zeros_like(x, dtype=np.float32)
    y[:, 0] = x[:, 0]
    for t in range(1, x.shape[1]):
        y[:, t] = beta * y[:, t - 1] + (1.0 - beta) * x[:, t]
    return y

def _measure_strength_amp_from_H(H_amp: np.ndarray, tech_list: List[str]) -> np.ndarray:
    H_amp = np.clip(H_amp.astype(np.float32), 1e-8, None)
    H_db = 20.0 * np.log10(H_amp)
    meas_db = np.zeros_like(H_db, dtype=np.float32)
    for i, tech in enumerate(tech_list):
        if tech == "ZigBee":
            sigma = float(cfg.ZIGBEE_MEAS_SIGMA_DB); clip = float(cfg.ZIGBEE_MEAS_CLIP_DB); q = float(cfg.ZIGBEE_MEAS_Q_DB)
        else:
            sigma = float(cfg.MEAS_SIGMA_DB_DEFAULT); clip = float(cfg.MEAS_CLIP_DB_DEFAULT); q = float(cfg.MEAS_Q_DB_DEFAULT)
        eps = (sigma * np.random.randn(H_db.shape[1])).astype(np.float32)
        eps = np.clip(eps, -clip, clip)
        m = (H_db[i, :] + eps).astype(np.float32)
        m = (np.round(m / max(1e-6, q)) * q).astype(np.float32)
        meas_db[i, :] = m
    meas_amp = (10.0 ** (meas_db / 20.0)).astype(np.float32)
    return np.clip(meas_amp, cfg.CSI_MIN_AMP, None)

def run_channel_chain(meta: pd.DataFrame, H_amp: np.ndarray, shadow_db: np.ndarray, interf_db: np.ndarray) -> Dict[str, np.ndarray]:
    n, T = H_amp.shape
    tech_list = [str(meta.loc[i, "tech"]) for i in range(n)]
    csi_meas = _measure_strength_amp_from_H(H_amp, tech_list=tech_list)
    base_snr = np.zeros((n, 1), dtype=np.float32)
    link_margin = np.zeros((n, 1), dtype=np.float32)
    node_off = meta["snr_node_offset_db"].to_numpy(dtype=np.float32).reshape(n, 1)
    for i in range(n):
        tech = str(meta.loc[i, "tech"])
        sf = int(meta.loc[i, "lora_sf"])
        base_snr[i, 0] = float(per_tech_base_snr_db(tech, lora_sf=sf))
        link_margin[i, 0] = float(per_tech_link_margin_db(tech))
    snr_db = base_snr + node_off + 20.0 * np.log10(csi_meas) + link_margin + shadow_db - interf_db
    per = compute_per_from_snr_db(meta, snr_db)
    lat = compute_latency_ms(meta, per)
    lat_smooth = smooth_ewma(lat, cfg.LAT_SMOOTH_BETA)
    return {"H_amp": H_amp.astype(np.float32), "csi_amp": csi_meas.astype(np.float32), "snr_db": snr_db.astype(np.float32),
            "packet_error": per.astype(np.float32), "latency": lat.astype(np.float32), "latency_smooth": lat_smooth.astype(np.float32)}

