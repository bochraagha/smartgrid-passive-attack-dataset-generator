def _markov_states(T: int, p_gb: float, p_bg: float, s0: int = 0) -> np.ndarray:
    s = np.zeros((T,), dtype=np.int8)
    s[0] = int(s0)
    for t in range(1, T):
        if s[t - 1] == 0:
            s[t] = 1 if (np.random.rand() < p_gb) else 0
        else:
            s[t] = 0 if (np.random.rand() < p_bg) else 1
    return s

def ar1_process(T: int, alpha: float, sigma: float, x0: float = 0.0) -> np.ndarray:
    out = np.zeros((T,), dtype=np.float32)
    out[0] = float(x0 + sigma * np.random.randn())
    for t in range(1, T):
        out[t] = float(alpha * out[t - 1] + sigma * np.random.randn())
    return out

def ar1_filter_stationary(x: np.ndarray, alpha: float) -> np.ndarray:
    x = x.astype(np.float32)
    y = np.zeros_like(x, dtype=np.float32)
    y[0] = x[0]
    a = float(np.clip(alpha, 0.0, 0.9999))
    s = float(np.sqrt(max(1e-8, 1.0 - a * a)))
    for t in range(1, x.shape[0]):
        y[t] = a * y[t - 1] + s * x[t]
    return y

def _trunc_norm(mu: float, sd: float, lo: float, hi: float) -> float:
    for _ in range(64):
        x = float(mu + sd * np.random.randn())
        if lo <= x <= hi:
            return x
    return float(np.clip(mu, lo, hi))

def _sample_mixture_truncnorm(weights, mus, sds, lo, hi) -> float:
    w = np.array(weights, dtype=np.float64)
    w = w / max(1e-12, w.sum())
    k = int(np.random.choice(np.arange(len(w)), p=w))
    return _trunc_norm(float(mus[k]), float(sds[k]), lo, hi)

