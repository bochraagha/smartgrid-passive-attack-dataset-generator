def rolling_entropy(x: np.ndarray, win: int, nbins: int = 12) -> np.ndarray:
    n, T = x.shape
    out = np.zeros((n, T), dtype=np.float32)
    xmin = np.quantile(x, 0.01, axis=1)
    xmax = np.quantile(x, 0.99, axis=1)
    for i in range(n):
        lo = float(xmin[i]); hi = float(xmax[i])
        if hi <= lo: hi = lo + 1e-3
        edges = np.linspace(lo, hi, nbins + 1)
        for t in range(T):
            a = max(0, t - win + 1)
            seg = x[i, a : t + 1]
            hist, _ = np.histogram(seg, bins=edges)
            p = hist.astype(np.float32)
            p = p / np.maximum(p.sum(), 1.0)
            nz = p[p > 0]
            out[i, t] = float(-np.sum(nz * np.log(nz)))
    return out

def rolling_mean(x: np.ndarray, win: int) -> np.ndarray:
    n, T = x.shape
    out = np.zeros((n, T), dtype=np.float32)
    c = np.cumsum(x, axis=1, dtype=np.float64)
    for t in range(T):
        a = max(0, t - win + 1)
        denom = (t - a + 1)
        s = c[:, t] - (c[:, a - 1] if a > 0 else 0.0)
        out[:, t] = (s / denom).astype(np.float32)
    return out

def compute_time_since_last_tx(tx_count: np.ndarray) -> np.ndarray:
    n, T = tx_count.shape
    out = np.zeros((n, T), dtype=np.int32)
    for i in range(n):
        gap = 0
        for t in range(T):
            if tx_count[i, t] > 0: gap = 0
            else: gap += 1
            out[i, t] = gap
    return out

def add_rolling_moments_in_df(df: pd.DataFrame, win: int) -> pd.DataFrame:
    out = df.copy().sort_values(["node", "t"]).reset_index(drop=True)
    out["csi_skewness"] = (
        out.groupby("node")["csi_amp"]
        .transform(lambda x: x.rolling(win, min_periods=3).skew())
        .fillna(0.0).astype("float32")
    )
    out["csi_kurtosis"] = (
        out.groupby("node")["csi_amp"]
        .transform(lambda x: x.rolling(win, min_periods=4).kurt())
        .fillna(0.0).astype("float32")
    )
    out = out.sort_values(["t", "node"]).reset_index(drop=True)
    return out

def _rolling_lag1_autocorr(x: np.ndarray, win: int) -> np.ndarray:
    x = x.astype(np.float64)
    T = x.shape[0]
    out = np.zeros((T,), dtype=np.float32)
    if T < 4 or win < 4: return out
    a = x[1:]; b = x[:-1]; w = win - 1
    if a.shape[0] < w: return out
    ca = np.cumsum(a); cb = np.cumsum(b); ca2 = np.cumsum(a * a); cb2 = np.cumsum(b * b); cab = np.cumsum(a * b)
    def _rsum(c, i0, i1): return c[i1] - (c[i0 - 1] if i0 > 0 else 0.0)
    for end in range(w - 1, a.shape[0]):
        i0 = end - (w - 1); i1 = end
        Sa = _rsum(ca, i0, i1); Sb = _rsum(cb, i0, i1); Sa2 = _rsum(ca2, i0, i1); Sb2 = _rsum(cb2, i0, i1); Sab = _rsum(cab, i0, i1)
        ma = Sa / w; mb = Sb / w
        cov = (Sab / w) - ma * mb
        va = (Sa2 / w) - ma * ma
        vb = (Sb2 / w) - mb * mb
        denom = math.sqrt(max(1e-12, va * vb))
        r = cov / denom
        out[end + 1] = float(np.clip(r, -1.0, 1.0))
    return out

def _rolling_var(x: np.ndarray, win: int) -> np.ndarray:
    x = x.astype(np.float64)
    T = x.shape[0]
    out = np.zeros((T,), dtype=np.float32)
    if win < 2: return out
    c = np.cumsum(x); c2 = np.cumsum(x * x)
    for t in range(T):
        a = max(0, t - win + 1); n = t - a + 1
        s = c[t] - (c[a - 1] if a > 0 else 0.0)
        s2 = c2[t] - (c2[a - 1] if a > 0 else 0.0)
        m = s / n
        v = max(0.0, (s2 / n) - m * m)
        out[t] = float(v)
    return out

def _cusum_pos_neg(z: np.ndarray, k: float, clip: float) -> Tuple[np.ndarray, np.ndarray]:
    z = z.astype(np.float32)
    T = z.shape[0]
    gp = np.zeros((T,), dtype=np.float32); gn = np.zeros((T,), dtype=np.float32)
    for t in range(1, T):
        gp[t] = max(0.0, gp[t - 1] + (z[t] - k))
        gn[t] = max(0.0, gn[t - 1] + (-z[t] - k))
        if gp[t] > clip: gp[t] = clip
        if gn[t] > clip: gn[t] = clip
    return gp, gn

def _ewma(z: np.ndarray, beta: float) -> np.ndarray:
    z = z.astype(np.float32)
    out = np.zeros_like(z, dtype=np.float32)
    out[0] = z[0]
    for t in range(1, z.shape[0]):
        out[t] = beta * out[t - 1] + (1.0 - beta) * z[t]
    return out

def add_sensitivity_features(df: pd.DataFrame) -> pd.DataFrame:
    out = df.copy().sort_values(["node", "t"]).reset_index(drop=True)
    out["csi_db"] = (20.0 * np.log10(np.clip(out["csi_amp"].to_numpy(np.float32), 1e-8, None))).astype("float32")
    out["dcsi_db"] = out.groupby("node")["csi_db"].diff(int(cfg.DELTA_WIN)).fillna(0.0).astype("float32")
    out["dsnr_db"] = out.groupby("node")["snr_db"].diff(int(cfg.DELTA_WIN)).fillna(0.0).astype("float32")
    out["snr_minus_csi_db"] = (out["snr_db"] - out["csi_db"]).astype("float32")
    per = np.clip(out["packet_error"].to_numpy(np.float32), 1e-6, 1.0 - 1e-6)
    out["per_logit"] = (np.log(per / (1.0 - per))).astype("float32")
    out["dper_logit"] = out.groupby("node")["per_logit"].diff(int(cfg.DELTA_WIN)).fillna(0.0).astype("float32")
    out["lat_log"] = np.log1p(np.maximum(0.0, out["latency_smooth"].to_numpy(np.float32))).astype("float32")
    out["dlat_log"] = out.groupby("node")["lat_log"].diff(int(cfg.DELTA_WIN)).fillna(0.0).astype("float32")
    csi_ac1 = np.zeros((len(out),), dtype=np.float32); csi_innov_var = np.zeros((len(out),), dtype=np.float32)
    snr_ac1 = np.zeros((len(out),), dtype=np.float32); snr_innov_var = np.zeros((len(out),), dtype=np.float32)
    for node, idx in out.groupby("node").groups.items():
        idx = np.array(list(idx), dtype=int)
        x_csi = out.loc[idx, "csi_db"].to_numpy(dtype=np.float32)
        r1 = _rolling_lag1_autocorr(x_csi, win=int(cfg.AC_WIN))
        csi_ac1[idx] = r1
        e = np.zeros_like(x_csi, dtype=np.float32); e[1:] = x_csi[1:] - (r1[1:] * x_csi[:-1])
        csi_innov_var[idx] = _rolling_var(e, win=int(cfg.INNOV_WIN))
        x_snr = out.loc[idx, "snr_db"].to_numpy(dtype=np.float32)
        r1s = _rolling_lag1_autocorr(x_snr, win=int(cfg.AC_WIN))
        snr_ac1[idx] = r1s
        es = np.zeros_like(x_snr, dtype=np.float32); es[1:] = x_snr[1:] - (r1s[1:] * x_snr[:-1])
        snr_innov_var[idx] = _rolling_var(es, win=int(cfg.INNOV_WIN))
    out["csi_ac1"] = csi_ac1.astype("float32"); out["csi_innov_var"] = csi_innov_var.astype("float32")
    out["snr_ac1"] = snr_ac1.astype("float32"); out["snr_innov_var"] = snr_innov_var.astype("float32")
    win = int(cfg.QSPREAD_WIN)
    out["dcsi_q90"] = out.groupby("node")["dcsi_db"].transform(lambda s: s.rolling(win, min_periods=5).quantile(0.90)).fillna(0.0).astype("float32")
    out["dcsi_q10"] = out.groupby("node")["dcsi_db"].transform(lambda s: s.rolling(win, min_periods=5).quantile(0.10)).fillna(0.0).astype("float32")
    out["dcsi_q75"] = out.groupby("node")["dcsi_db"].transform(lambda s: s.rolling(win, min_periods=5).quantile(0.75)).fillna(0.0).astype("float32")
    out["dcsi_q25"] = out.groupby("node")["dcsi_db"].transform(lambda s: s.rolling(win, min_periods=5).quantile(0.25)).fillna(0.0).astype("float32")
    out["dcsi_qspread"] = (out["dcsi_q90"] - out["dcsi_q10"]).astype("float32")
    out["dcsi_iqr"] = (out["dcsi_q75"] - out["dcsi_q25"]).astype("float32")
    w = int(cfg.ROBUST_WIN)
    def _mad(s):
        med = s.rolling(w, min_periods=5).median()
        return (s - med).abs().rolling(w, min_periods=5).median()
    out["mad_snr_minus_csi_db"] = out.groupby("node")["snr_minus_csi_db"].transform(_mad).fillna(0.0).astype("float32")
    out["mad_lat_log"] = out.groupby("node")["lat_log"].transform(_mad).fillna(0.0).astype("float32")
    return out.sort_values(["t", "node"]).reset_index(drop=True)

