def generate_split(meta: pd.DataFrame, A: np.ndarray, split_name: str, T_export: int, seed: int) -> Tuple[pd.DataFrame, List[Dict]]:
    seed_all(seed)
    n = meta.shape[0]
    T = int(cfg.BURN_IN + T_export)
    tx = gen_tx_count(n, T, meta)
    H, alpha_base, sigma_base, h_cplx = gen_channel_amp_per_node(meta, T, mean=1.0)
    shadow = gen_shadowing_db(meta, T)
    interf = gen_interference_db(meta, T)
    ch = run_channel_chain(meta, H_amp=H, shadow_db=shadow, interf_db=interf)
    streams: Dict[str, np.ndarray] = {**ch, "shadow_db": shadow.astype(np.float32), "interf_db": interf.astype(np.float32), "h_cplx": h_cplx}
    tx_export = tx[:, cfg.BURN_IN:]
    y_export, windows = place_attack_windows(tx_count=tx_export, meta=meta, A=A, T=T_export, target_frac=cfg.TARGET_ATTACK_FRAC, split_name=split_name)
    windows_global: List[Dict] = []
    for w in windows:
        wg = dict(w)
        wg["s0"] = int(w["s0"] + cfg.BURN_IN); wg["s1"] = int(w["s1"] + cfg.BURN_IN)
        wg["t0"] = int(w["t0"] + cfg.BURN_IN); wg["t1"] = int(w["t1"] + cfg.BURN_IN)
        windows_global.append(wg)
    apply_attack_perturbations(meta=meta, streams=streams, windows=windows_global, tx_count_full=tx, alpha_base=alpha_base, sigma_base=sigma_base)
    # ---- Phase features from complex channel (label-safe) ----
    h_final = streams["h_cplx"]
    phase_rad = np.angle(h_final).astype(np.float32)  # wrapped in [-pi, pi]
    phase_sin = np.sin(phase_rad).astype(np.float32)
    phase_cos = np.cos(phase_rad).astype(np.float32)
    phase_unw = np.unwrap(phase_rad.astype(np.float64), axis=1).astype(np.float32)
    dphase = np.zeros_like(phase_rad, dtype=np.float32)
    dphase[:, 0] = 0.0
    dphase[:, 1:] = (phase_unw[:, 1:] - phase_unw[:, :-1]).astype(np.float32)
    streams["phase_rad"] = phase_rad
    streams["phase_sin"] = phase_sin
    streams["phase_cos"] = phase_cos
    streams["dphase"] = dphase

    streams_export = {k: v[:, cfg.BURN_IN:] for k, v in streams.items()
                      if isinstance(v, np.ndarray) and v.ndim == 2 and k != "h_cplx" and np.issubdtype(v.dtype, np.floating)}
    df = build_split_df(meta, streams_export, tx_export, y_export, split_name)
    for w in windows:
        w["n_nodes"] = int(len(w["nodes"]))
        w["L_core"] = int(w["t1"] - w["t0"])
        w["L_labeled"] = int(w["s1"] - w["s0"])
    return df, windows

