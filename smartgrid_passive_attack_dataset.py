def _cn(size: int) -> np.ndarray:
    return ((np.random.randn(size).astype(np.float32) + 1j*np.random.randn(size).astype(np.float32)) / np.sqrt(2.0)).astype(np.complex64)

def _amp_scale_to_mean(mean_amp: float) -> float:
    return float(mean_amp / max(1e-6, float(np.sqrt(np.pi) / 2.0)))

def gen_channel_amp_per_node(meta: pd.DataFrame, T: int, mean: float = 1.0) -> Tuple[np.ndarray, np.ndarray, np.ndarray, np.ndarray]:
    n = meta.shape[0]
    h = np.zeros((n, T), dtype=np.complex64)
    rho_base = np.zeros((n,), dtype=np.float32)
    sigma_base = np.ones((n,), dtype=np.float32)
    amp_scale = _amp_scale_to_mean(float(mean))
    for i in range(n):
        tech = str(meta.loc[i, "tech"])
        rho, _, _, _ = per_tech_channel_params(tech)
        rho = float(np.clip(rho, 0.0, 0.9999))
        rho_base[i] = rho
        h[i, 0] = _cn(1)[0]
        for t in range(1, T):
            w = _cn(1)[0]
            h[i, t] = (rho * h[i, t-1] + np.sqrt(max(1e-8, 1.0 - rho*rho)) * w).astype(np.complex64)
        if tech == "PLC" and T > 50:
            n_bursts = int(np.random.randint(2, 6))
            for _ in range(n_bursts):
                s0 = int(np.random.randint(0, T - 10))
                Lb = int(np.random.randint(6, 25))
                s1 = min(T, s0 + Lb)
                h[i, s0:s1] = (h[i, s0:s1] * (0.85 + 0.10 * np.random.rand())).astype(np.complex64)
        if tech == "LTE":
            drift = 0.02 * np.cumsum(np.random.randn(T).astype(np.float32)) / max(1, np.sqrt(T))
            h[i, :] = (h[i, :] * (1.0 + drift).astype(np.float32)).astype(np.complex64)
    H_amp = (np.abs(h).astype(np.float32) * amp_scale).astype(np.float32)
    H_amp = np.clip(H_amp, 1e-3, None).astype(np.float32)
    return H_amp, rho_base, sigma_base, h

