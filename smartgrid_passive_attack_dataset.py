def attack_eligible_nodes(meta: pd.DataFrame) -> List[int]:
    allowed = set(cfg.ATTACK_ELIGIBLE_TECH)
    return [int(r["node"]) for _, r in meta.iterrows() if str(r["tech"]) in allowed]

def pick_connected_group(A: np.ndarray, eligible: List[int], group_size: int, seed: Optional[int] = None) -> List[int]:
    if group_size <= 1:
        return [int(seed) if seed is not None else int(np.random.choice(eligible))]
    eligible_set = set(int(x) for x in eligible)
    if seed is None:
        seed = int(np.random.choice(eligible))
    else:
        seed = int(seed)
        if seed not in eligible_set:
            seed = int(np.random.choice(eligible))
    group = [seed]; frontier = [seed]
    while len(group) < group_size and frontier:
        u = int(frontier.pop(0))
        nbrs = np.where(A[u] > 0)[0].astype(int).tolist()
        nbrs = [v for v in nbrs if (v in eligible_set and v not in group)]
        random.shuffle(nbrs)
        for v in nbrs:
            group.append(v); frontier.append(v)
            if len(group) >= group_size: break
    if len(group) < group_size:
        rest = [v for v in eligible if v not in group]
        random.shuffle(rest)
        group.extend(rest[: (group_size - len(group))])
    return group

def place_attack_windows(tx_count: np.ndarray, meta: pd.DataFrame, A: np.ndarray, T: int, target_frac: float, split_name: str) -> Tuple[np.ndarray, List[Dict]]:
    n = meta.shape[0]
    y = np.zeros((n, T), dtype=np.int8)
    eligible = attack_eligible_nodes(meta)
    if len(eligible) == 0: return y, []
    active = (tx_count > 0).astype(np.int16)
    active_pref = np.concatenate([np.zeros((n, 1), dtype=np.int32), np.cumsum(active, axis=1, dtype=np.int32)], axis=1)
    def active_frac(node: int, a: int, b: int) -> float:
        if b <= a: return 0.0
        a = max(0, a); b = min(T, b)
        cnt = int(active_pref[node, b] - active_pref[node, a])
        return cnt / float(b - a)
    active_rows_node = {nd: int(active[nd, :].sum()) for nd in eligible}
    target_rows_node = {nd: int(math.ceil(target_frac * max(1, active_rows_node[nd]))) for nd in eligible}
    attacked_rows_node = {nd: 0 for nd in eligible}
    def node_deficit(nd: int) -> int: return target_rows_node[nd] - attacked_rows_node[nd]
    def node_is_saturated(nd: int) -> bool: return attacked_rows_node[nd] >= int(1.15 * target_rows_node[nd])
    windows: List[Dict] = []; used_keys = set()
    max_global_tries = 14000; tries = 0
    while tries < max_global_tries and any(node_deficit(nd) > 0 for nd in eligible):
        tries += 1
        under = [nd for nd in eligible if node_deficit(nd) > 0]
        if not under: break
        weights = np.array([max(1, node_deficit(nd)) for nd in under], dtype=np.float64); weights = weights / weights.sum()
        anchor = int(np.random.choice(np.array(under, dtype=int), p=weights))
        L = int(np.random.randint(cfg.WIN_CORE_MIN, cfg.WIN_CORE_MAX + 1))
        L_ext = int(cfg.LEAD + L + cfg.TAIL + cfg.HYST)
        if L_ext >= T - 2: continue
        group_size = int(np.random.randint(cfg.GROUP_MIN, cfg.GROUP_MAX + 1))
        group_size = min(group_size, len(eligible))
        group = pick_connected_group(A, eligible, group_size, seed=anchor)
        if sum(1 for nd in group if node_is_saturated(int(nd))) >= max(2, len(group) // 2): continue
        gidx = np.array(group, dtype=int)
        ok_start: Optional[Tuple[int, int, int, int]] = None
        for _ in range(max(1, cfg.ACTIVITY_RESAMPLE_TRIES)):
            s0 = int(np.random.randint(0, T - L_ext)); s1 = int(s0 + L_ext)
            t0 = int(s0 + cfg.LEAD); t1 = int(t0 + L)
            if cfg.UNIQUE_KEY:
                key = (split_name, t0, tuple(sorted(group)))
                if key in used_keys: continue
            if (not cfg.ALLOW_OVERLAP) and np.any(y[gidx, s0:s1] == 1): continue
            ok = True
            for nd in group:
                nd = int(nd)
                if cfg.MIN_ACTIVE_FRAC_CORE > 0 and active_frac(nd, t0, t1) < cfg.MIN_ACTIVE_FRAC_CORE: ok = False; break
                if cfg.MIN_ACTIVE_FRAC_LABELED > 0 and active_frac(nd, s0, s1) < cfg.MIN_ACTIVE_FRAC_LABELED: ok = False; break
                if node_is_saturated(nd): ok = False; break
            if ok:
                ok_start = (s0, s1, t0, t1)
                if cfg.UNIQUE_KEY: used_keys.add(key)
                break
        if ok_start is None: continue
        s0, s1, t0, t1 = ok_start
        before_vec = y[gidx, :].sum(axis=1).astype(np.int32)
        mask_active = (tx_count[gidx, s0:s1] > 0)
        y[gidx, s0:s1] = np.maximum(y[gidx, s0:s1], mask_active.astype(np.int8))
        after_vec = y[gidx, :].sum(axis=1).astype(np.int32)
        delta_vec = (after_vec - before_vec)
        for k2, nd in enumerate(group):
            nd = int(nd)
            if nd in attacked_rows_node:
                attacked_rows_node[nd] += int(delta_vec[k2])
        kdrop_db = float(sample_kdrop_db())
        alpha_drop, sigma_mult = map_kdrop_to_surrogates(kdrop_db)
        sh_loss = float(sample_shadow_loss_db())
        windows.append({"split": split_name, "s0": int(s0), "s1": int(s1), "t0": int(t0), "t1": int(t1),
                        "nodes": group, "reason": "COHERENCE_DROP",
                        "kdrop_db": float(kdrop_db), "alpha_drop": float(alpha_drop),
                        "sigma_mult": float(sigma_mult), "shadow_loss_db": float(sh_loss)})
    return y, windows

def _rician_amp(K_db: float, size: int) -> np.ndarray:
    K = 10.0 ** (float(K_db) / 10.0)
    s = math.sqrt(K / (K + 1.0))
    sig = math.sqrt(1.0 / (2.0 * (K + 1.0)))
    x = s + sig * np.random.randn(size).astype(np.float32)
    y = sig * np.random.randn(size).astype(np.float32)
    return np.sqrt(x * x + y * y).astype(np.float32)

def apply_attack_perturbations(meta: pd.DataFrame, streams: Dict[str, np.ndarray], windows: List[Dict],
                               tx_count_full: np.ndarray, alpha_base: np.ndarray, sigma_base: np.ndarray) -> None:
    if "h_cplx" not in streams:
        raise KeyError("streams must include 'h_cplx'.")
    h = streams["h_cplx"].copy().astype(np.complex64)
    shadow = streams["shadow_db"].copy()
    interf = streams["interf_db"].copy()
    amp_scale = _amp_scale_to_mean(1.0)
    for w in windows:
        s0, s1 = int(w["s0"]), int(w["s1"])
        nodes = list(w["nodes"])
        Llab = max(1, s1 - s0)
        ramp_len = max(1, int(round(cfg.RAMP_FRAC * Llab)))
        ramp = np.ones((Llab,), dtype=np.float32)
        if ramp_len > 1:
            ramp[:ramp_len] = np.linspace(0.0, 1.0, ramp_len).astype(np.float32)
        rho_drop = float(w["alpha_drop"])
        sigma_mult = float(w["sigma_mult"])
        sh_loss = float(w["shadow_loss_db"])
        ge = _markov_states(Llab, p_gb=cfg.ATTACK_GE_P_GB, p_bg=cfg.ATTACK_GE_P_BG, s0=0).astype(np.float32)
        for nd in nodes:
            tech = str(meta.loc[nd, "tech"])
            act = (tx_count_full[nd, s0:s1] > 0).astype(np.float32)
            if act.sum() <= 0: continue
            shadow[nd, s0:s1] = shadow[nd, s0:s1] - (sh_loss * ramp * act)
            rho0 = float(alpha_base[nd])
            rho_a = float(np.clip(rho0 * (1.0 - rho_drop), 0.0, 0.9999))
            prev = h[nd, s0 - 1] if s0 > 0 else h[nd, 0]
            for k2, t in enumerate(range(s0, s1)):
                if act[k2] <= 0.0:
                    prev = h[nd, t]; continue
                bad = float(ge[k2])
                rho_eff = float(np.clip(rho_a * (1.0 - cfg.ATTACK_BAD_ALPHA_EXTRA * bad), 0.0, 0.9999))
                innov_scale = float(np.clip(sigma_mult * (1.0 + cfg.ATTACK_BAD_SIGMA_EXTRA * bad), 0.5, 6.0))
                w_c = _cn(1)[0]
                new_val = (rho_eff * prev + np.sqrt(max(1e-8, 1.0 - rho_eff * rho_eff)) * innov_scale * w_c).astype(np.complex64)
                h[nd, t] = ((1.0 - ramp[k2]) * h[nd, t] + ramp[k2] * new_val).astype(np.complex64)
                prev = h[nd, t]
            if cfg.WIFI_ENABLE_REFLECTION_COMPONENT and (tech == "WiFi"):
                Ttot = h.shape[1]
                pre = h[nd, max(0, s0-200):s0+1] if s0 > 5 else h[nd, :min(Ttot, 200)]
                hu = float(np.median(np.abs(pre)))
                att = 10.0 ** (-float(cfg.WIFI_REFLECT_ATTEN_DB) / 20.0)
                A_ref = float(cfg.WIFI_REFLECT_SCALE) * hu * att
                ha_amp = _rician_amp(cfg.WIFI_REFLECT_RICIAN_K_DB, Llab).astype(np.float32) * float(A_ref)
                phi = (2.0 * np.pi * np.random.rand(Llab)).astype(np.float32)
                ha_cplx = (ha_amp * (np.cos(phi) + 1j*np.sin(phi))).astype(np.complex64)
                h[nd, s0:s1] = (h[nd, s0:s1] + (ha_cplx * ramp * act).astype(np.complex64)).astype(np.complex64)
    H_amp = (np.abs(h).astype(np.float32) * amp_scale).astype(np.float32)
    H_amp = np.clip(H_amp, 1e-3, None).astype(np.float32)
    out = run_channel_chain(meta, H_amp=H_amp, shadow_db=shadow, interf_db=interf)
    streams["H_amp"] = out["H_amp"]; streams["csi_amp"] = out["csi_amp"]; streams["snr_db"] = out["snr_db"]
    streams["packet_error"] = out["packet_error"]; streams["latency"] = out["latency"]; streams["latency_smooth"] = out["latency_smooth"]
    streams["shadow_db"] = shadow; streams["interf_db"] = interf; streams["h_cplx"] = h

