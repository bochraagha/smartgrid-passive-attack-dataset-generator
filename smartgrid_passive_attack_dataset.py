def build_split_df(meta: pd.DataFrame, streams: Dict[str, np.ndarray], tx_count: np.ndarray, attack_label: np.ndarray, split_name: str) -> pd.DataFrame:
    n, T = tx_count.shape
    t_idx = np.arange(T, dtype=np.int32)
    rows = []
    for node in range(n):
        df = pd.DataFrame({
            "t": t_idx, "node": node, "split": split_name,
            "attack_label": attack_label[node, :].astype(np.int8),
            "tx_count": tx_count[node, :].astype(np.int32),
            "csi_amp": streams["csi_amp"][node, :].astype(np.float32),
            "snr_db": streams["snr_db"][node, :].astype(np.float32),
            "packet_error": streams["packet_error"][node, :].astype(np.float32),
            "latency": streams["latency"][node, :].astype(np.float32),
            "latency_smooth": streams["latency_smooth"][node, :].astype(np.float32),
            "shadow_db": streams["shadow_db"][node, :].astype(np.float32),
            "interf_db": streams["interf_db"][node, :].astype(np.float32),
            "phase_rad": streams["phase_rad"][node, :].astype(np.float32),
            "phase_sin": streams["phase_sin"][node, :].astype(np.float32),
            "phase_cos": streams["phase_cos"][node, :].astype(np.float32),
            "dphase": streams["dphase"][node, :].astype(np.float32),
        })
        rows.append(df)
    out = pd.concat(rows, ignore_index=True)
    out_sorted = out.sort_values(["node", "t"]).reset_index(drop=True)
    csi_mat = out_sorted["csi_amp"].to_numpy(dtype=np.float32).reshape(n, T)
    ent = rolling_entropy(csi_mat, win=cfg.ENT_WIN)
    csi_smooth = rolling_mean(csi_mat, win=cfg.DRIFT_WIN)
    drift = (csi_mat - csi_smooth).astype(np.float32)
    out_sorted["csi_entropy"] = ent.T.reshape(-1)
    out_sorted["csi_drift"] = drift.T.reshape(-1)
    tx_mat = out_sorted["tx_count"].to_numpy(dtype=np.int32).reshape(n, T)
    tsl = compute_time_since_last_tx(tx_mat)
    out_sorted["time_since_last_tx"] = tsl.T.reshape(-1)
    out_sorted = add_rolling_moments_in_df(out_sorted, win=cfg.MOMENT_WIN)
    out_sorted = out_sorted.sort_values(["t", "node"]).reset_index(drop=True)
    return out_sorted

