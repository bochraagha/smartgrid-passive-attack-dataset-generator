def gen_shadowing_db(meta: pd.DataFrame, T: int) -> np.ndarray:
    n = meta.shape[0]
    out = np.zeros((n, T), dtype=np.float32)
    g = ar1_process(T, alpha=cfg.ENV_GLOBAL_ALPHA, sigma=cfg.ENV_SHADOW_SIGMA_GLOBAL)
    layers = meta["layer"].unique().tolist()
    layer_proc: Dict[str, np.ndarray] = {str(ly): ar1_process(T, alpha=cfg.ENV_LAYER_ALPHA, sigma=cfg.ENV_SHADOW_SIGMA_LAYER) for ly in layers}
    if cfg.USE_GUDMUNDSON and (GLOBAL_CORR_CHOL is not None):
        z_corr = (GLOBAL_CORR_CHOL @ np.random.randn(n, T).astype(np.float32)).astype(np.float32)
    else:
        z_corr = np.random.randn(n, T).astype(np.float32)
    for i in range(n):
        tech = str(meta.loc[i, "tech"])
        layer = str(meta.loc[i, "layer"])
        scen = _scenario_for_node(layer, tech)
        los_p = float(cfg.LOS_P_BY_SCENARIO.get(scen, 0.7))
        is_los = (np.random.rand() < los_p)
        key = "LOS" if is_los else "NLOS"
        sigma_target = float(cfg.TR38901_SF[scen][key])
        dcor = float(cfg.TR38901_DCOR_M[scen][key])
        alpha_sf = _alpha_from_dcor(dcor, layer)
        local_raw = (sigma_target * z_corr[i, :]).astype(np.float32)
        local = ar1_filter_stationary(local_raw, alpha=alpha_sf)
        mix = (
            cfg.ENV_SHADOW_W_GLOBAL * g
            + cfg.ENV_SHADOW_W_LAYER * layer_proc[layer]
            + (1.0 - cfg.ENV_SHADOW_W_GLOBAL - cfg.ENV_SHADOW_W_LAYER) * local
        ).astype(np.float32)
        s = float(np.std(mix))
        out[i, :] = mix if s < 1e-3 else (mix * (sigma_target / s)).astype(np.float32)
    return out

