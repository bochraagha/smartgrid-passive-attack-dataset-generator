def _all_pairs_shortest_paths(A: np.ndarray) -> np.ndarray:
    n = A.shape[0]
    dist = np.full((n, n), np.inf, dtype=np.float32)
    np.fill_diagonal(dist, 0.0)
    for i in range(n):
        dist[i, A[i] > 0] = 1.0
    for k in range(n):
        dist = np.minimum(dist, dist[:, [k]] + dist[[k], :])
    dist[~np.isfinite(dist)] = float(n)
    return dist

def _corr_from_hops(dist: np.ndarray, L: float) -> np.ndarray:
    return np.exp(-dist / max(1e-6, float(L))).astype(np.float32)

def _robust_cholesky(R: np.ndarray) -> np.ndarray:
    eps = 1e-6
    I = np.eye(R.shape[0], dtype=np.float32)
    for _ in range(8):
        try:
            return np.linalg.cholesky((R + eps * I).astype(np.float32)).astype(np.float32)
        except np.linalg.LinAlgError:
            eps *= 10.0
    w, V = np.linalg.eigh(R.astype(np.float64))
    w = np.clip(w, 1e-6, None)
    Rpd = (V @ np.diag(w) @ V.T).astype(np.float32)
    return np.linalg.cholesky(Rpd).astype(np.float32)

