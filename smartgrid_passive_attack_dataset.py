def _stable_rvs(alpha: float, beta: float, gamma: float, delta: float, size: int) -> np.ndarray:
    alpha = float(alpha); beta = float(np.clip(beta, -1.0, 1.0))
    U = np.pi * (np.random.rand(size).astype(np.float64) - 0.5)
    W = np.random.exponential(scale=1.0, size=size).astype(np.float64)
    if abs(alpha - 1.0) > 1e-6:
        zeta = beta * math.tan(math.pi * alpha / 2.0)
        B = math.atan(zeta) / alpha
        S = (1.0 + zeta * zeta) ** (1.0 / (2.0 * alpha))
        X = S * (np.sin(alpha * (U + B)) / (np.cos(U) ** (1.0 / alpha))) * (
            (np.cos(U - alpha * (U + B)) / W) ** ((1.0 - alpha) / alpha)
        )
        return (gamma * X + delta).astype(np.float32)
    else:
        part1 = (2.0 / np.pi) * ((np.pi / 2.0 + beta * U) * np.tan(U) - beta * np.log((np.pi / 2.0 * W * np.cos(U)) / (np.pi / 2.0 + beta * U)))
        return (gamma * part1 + delta).astype(np.float32)

def plc_impulsive_spikes_db(T: int) -> np.ndarray:
    spikes = np.zeros((T,), dtype=np.float32)
    if cfg.PLC_USE_BGM:
        st = _markov_states(T, p_gb=cfg.PLC_BGM_P01, p_bg=cfg.PLC_BGM_P10, s0=0).astype(np.int8)
        sig0 = math.sqrt(max(1e-12, float(cfg.PLC_BGM_SIGMA0_SQ)))
        sig1 = math.sqrt(max(1e-12, float(cfg.PLC_BGM_SIGMA1_SQ)))
        g = np.random.randn(T).astype(np.float32)
        sig = np.where(st == 1, sig1, sig0).astype(np.float32)
        pwr = (sig * g) ** 2
        spikes += (10.0 * np.log10(1.0 + pwr)).astype(np.float32)
    if cfg.PLC_USE_ALPHA_STABLE and (cfg.PLC_STABLE_IMP_PER_MIN > 0):
        p_imp = float(cfg.PLC_STABLE_IMP_PER_MIN) / 60.0
        mask = (np.random.rand(T) < p_imp).astype(np.float32)
        if mask.sum() > 0:
            x = _stable_rvs(cfg.PLC_STABLE_ALPHA, cfg.PLC_STABLE_BETA, cfg.PLC_STABLE_GAMMA, cfg.PLC_STABLE_DELTA, T)
            pwr = (cfg.PLC_STABLE_ATTEN * np.abs(x).astype(np.float32)) ** 2
            spikes += mask * (10.0 * np.log10(1.0 + pwr)).astype(np.float32)
    spikes = np.clip(spikes, float(cfg.PLC_IMP_CLIP_DB[0]), float(cfg.PLC_IMP_CLIP_DB[1])).astype(np.float32)
    return spikes

